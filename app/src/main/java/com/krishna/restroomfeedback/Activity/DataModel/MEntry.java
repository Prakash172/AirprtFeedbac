package com.krishna.restroomfeedback.Activity.DataModel;

public class MEntry{
    public int valueX, valueY;

    public MEntry(int valueX, int valueY) {
        this.valueX = valueX;
        this.valueY = valueY;
    }

    public int getValueX() {
        return valueX;
    }

    public int getValueY() {
        return valueY;
    }

}
